async function assertFile(path: string) {
  await access(join(root, path))
}

async function readRepoFile(path: string) {
  return readFile(join(root, path), 'utf8')
}

async function collectDecisionMarkdownFiles() {
  const decisionEntries = await readdir(join(root, 'memory/decisions'), { withFileTypes: true })
  return decisionEntries
    .filter((entry) => entry.isFile() && entry.name.endsWith('.md'))
    .map((entry) => `memory/decisions/${entry.name}`)
    .sort()
}

async function collectMarkdownHeadingFiles() {
  return [...markdownHeadingBaseFiles, ...(await collectDecisionMarkdownFiles())]
}

function requireIncludes(content: string, values: string[], file: string) {
  const missing = values.filter((value) => !content.includes(value))
  if (missing.length > 0) {
    throw new Error(`${file} ยังไม่มีข้อความที่ต้องมี: ${missing.join(', ')}`)
  }
}

function forbidIncludes(content: string, values: string[], file: string) {
  const present = values.filter((value) => content.includes(value))
  if (present.length > 0) {
    throw new Error(`${file} ยังมีข้อความเก่าที่ต้องเอาออก: ${present.join(', ')}`)
  }
}

function splitPackageScriptCommands(...scripts: string[]) {
  return scripts.flatMap((script) =>
    script
      .split(/\s*&&\s*/)
      .map((command) => command.trim())
      .filter(Boolean),
  )
}

function collectSimpleWorkflowRunCommands(content: string) {
  return content
    .split(/\r?\n/)
    .map((line) => line.trim().match(/^(?:-\s*)?run:\s*(.+)$/)?.[1]?.trim())
    .filter((command): command is string => Boolean(command) && command !== '|' && command !== '>')
}

function requireWorkflowRunCommands(content: string, commands: string[], file: string) {
  const actual = new Set(collectSimpleWorkflowRunCommands(content))
  const missing = commands.filter((command) => !actual.has(command))
  if (missing.length > 0) {
    throw new Error(`${file} ต้องมี workflow run command แบบ exact: ${missing.join(', ')}`)
  }
}

function assertThaiFirstMarkdownHeadings(content: string, file: string) {
  const allowedTechnicalHeadings = [/^#{1,6}\s+API\b/, /^#{1,6}\s+URL\b/, /^#\s+\d{4}\s+-\s+/]
  const offenders = content
    .split(/\r?\n/)
    .map((line, index) => ({ line, number: index + 1 }))
    .filter(({ line }) => /^#{1,6}\s+[A-Za-z0-9]/.test(line))
    .filter(({ line }) => !allowedTechnicalHeadings.some((pattern) => pattern.test(line)))

  if (offenders.length > 0) {
    const details = offenders.map(({ line, number }) => `${number}: ${line}`).join('; ')
    throw new Error(`${file} ยังมีหัวข้อ Markdown ที่ไม่ได้ขึ้นต้นแบบ Thai-first: ${details}`)
  }
}

